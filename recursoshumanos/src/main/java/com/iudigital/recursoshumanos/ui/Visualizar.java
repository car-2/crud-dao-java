    private void upData() {
        DefaultTableModel modelTable = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modelTable.setColumnIdentifiers(new Object[]{"id", "type identification", "name", "last name", "marital status", "genre", "address", "telphone", "datebirth"});

        Vector<Employee> vector = new Vector<>();
        EmployeeDao employeeDao = new EmployeeDaoImplementation();
        table.setModel(modelTable);

        for (Employee employee : employeeDao.list()) {
            modelTable.addRow(new Object[]{employee.getId(), employee.getTypeId(), employee.getName(), employee.getLastName(), employee.getMaritalStatus(), employee.getGenre(), employee.getAddress(), employee.getTelphone(), employee.getDateBirth()});
        }
    }
